class IntentEngine:
    """意图识别引擎"""

    def __init__(self, retriever):
        self.retriever = retriever

    def detect_scenario(self, query: str) -> str:
        """优先级规则引擎检测咨询场景"""
        best_scenario = "general"
        best_score = 0

        for keywords, scenario, weight in SCENARIO_RULES:
            if best_score >= 20:  # 最高权重，无需继续
                break
            for kw in keywords:
                if kw in query:
                    score = weight
                    if score > best_score:
                        best_score = score
                        best_scenario = scenario
                    break

        return best_scenario

    def extract_attraction(self, query: str, context: dict = None) -> str:
        """提取景点名（含别名 + 上下文）"""
        all_attrs = self.retriever.attractions

        # 1. 精确匹配原始景点名
        for att in all_attrs:
            if att["name"] in query:
                return att["name"]

        # 2. 别名匹配
        for alias, full_name in ATTRACTION_ALIASES.items():
            if alias in query:
                return full_name

        # 3. 上下文指代消解
        if context and context.get("attraction_name"):
            # 指代词检测
            pronouns = ["它", "这个", "那个", "该景点", "这里", "那里", "这", "那", "ta"]
            for p in pronouns:
                if p in query:
                    return context["attraction_name"]
            # "推荐几个" -> 复用上下文景点
            implicit_refs = ["推荐几个", "还有呢", "还有吗", "其他的", "附近", "周边"]
            for ref in implicit_refs:
                if ref in query and context.get("attraction_name"):
                    return context["attraction_name"]

        return None

    def extract_province(self, query: str) -> str:
        """提取省份"""
        # 别名优先
        for alias, province in PROVINCE_ALIASES.items():
            if alias in query:
                return province
        # 精确匹配
        for p in self.retriever.get_provinces():
            if p in query:
                return p
        return None

    def extract_city(self, query: str) -> str:
        """提取城市"""
        for alias, city in CITY_ALIASES.items():
            if alias in query:
                return city
        cities = sorted(set(a["city"] for a in self.retriever.attractions), key=lambda x: -len(x))
        for c in cities:
            if c in query:
                return c
        return None

    def extract_category(self, query: str) -> str:
        """提取分类意图"""
        cat_map = {
            "自然风光": ["自然", "山水", "草原", "沙漠", "森林",
                        "风景", "湖", "瀑布", "冰雪", "自然风光"],
            "历史文化": ["历史", "文化", "古迹", "古建筑", "寺庙", "石窟", "博物馆",
                        "古城", "文化遗产", "历史文", "文物"],
            "主题乐园": ["乐园", "游乐", "主题公园", "迪士尼", "长隆", "带孩子", "玩"],
            "都市风情": ["都市", "城市", "逛街", "购物", "繁华", "地标"],
            "现代建筑": ["现代建筑", "高楼", "观光塔", "摩天轮", "大桥", "建筑"],
        }
        for cat, keywords in cat_map.items():
            for kw in keywords:
                if kw in query:
                    return cat
        return None

    def extract_season(self, query: str) -> str:
        """提取季节意图"""
        if any(w in query for w in ["夏季", "夏天", "避暑", "暑假", "热"]):
            return "夏季"
        if any(w in query for w in ["冬季", "冬天", "冰雪", "滑雪", "冷", "寒假"]):
            return "冬季"
        if any(w in query for w in ["春季", "春天", "赏花", "踏青"]):
            return "春季"
        if any(w in query for w in ["秋季", "秋天", "秋色", "红叶", "枫叶"]):
            return "秋季"
        return None

    def extract_all(self, query: str, context: dict = None) -> dict:
        """一次性提取所有实体"""
        return {
            "scenario": self.detect_scenario(query),
            "attraction_name": self.extract_attraction(query, context),
            "province": self.extract_province(query),
            "city": self.extract_city(query),
            "category": self.extract_category(query),
            "season": self.extract_season(query),
        }
