def _city_key(province, city):
    """获取城市餐厅数据的键"""
    # 尝试直接匹配城市名
    if city and city in CITY_RESTAURANTS:
        return city
    # 部分城市在数据中可能以省份同名
    if province in CITY_RESTAURANTS:
        return province
    return None


def _get_restaurants(province, city):
    """获取城市餐厅数据，fallback到省份级"""
    key = _city_key(province, city)
    if key:
        return CITY_RESTAURANTS[key]
    return GENERIC_RESTAURANTS


