def apply_enhancements(attractions: list) -> list:
    """将手工润色数据应用到景点列表"""
    from copy import deepcopy

    enhanced = deepcopy(attractions)
    for att in enhanced:
        name = att["name"]
        enh = ATTRACTION_ENHANCEMENTS.get(name, {})

        for field in ["basic_info", "travel_guide", "transport", "culture", "food"]:
            if enh.get(field):
                att[field] = enh[field]

        if enh.get("related"):
            # 将关联景点ID存入临时字段
            related_ids = []
            for rname in enh["related"]:
                for a2 in attractions:
                    if a2["name"] == rname:
                        related_ids.append(a2["id"])
                        break
            att["related_ids"] = related_ids

        if enh.get("location"):
            att["location"] = enh["location"]

    # 补充坐标
    for att in enhanced:
        if "location" not in att or not att["location"]:
            if att["name"] in EXTRA_LOCATIONS:
                att["location"] = EXTRA_LOCATIONS[att["name"]]

    return enhanced
