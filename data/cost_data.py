TICKET_RANGES = {
    "历史文化": (30, 150),
    "自然风光": (80, 250),
    "主题乐园": (200, 500),
    "都市风情": (0, 100),
    "现代建筑": (50, 200),
}


def estimate_trip_cost(province: str, days: int, category: str = None) -> dict:
    """估算旅行费用"""
    est = COST_ESTIMATES.get(province, {"住宿": (200, 500), "餐饮": (50, 120), "交通": (20, 50)})
    ticket_range = TICKET_RANGES.get(category, (50, 150))

    accommodation = est["住宿"]
    food = est["餐饮"]
    transport = est["交通"]

    return {
        "province": province,
        "days": days,
        "住宿": (accommodation[0] * days, accommodation[1] * days),
        "餐饮": (food[0] * days, food[1] * days),
        "交通": (transport[0] * days, transport[1] * days),
        "门票": (ticket_range[0] * days, ticket_range[1] * days),
        "总计": (
            accommodation[0] * days + food[0] * days + transport[0] * days + ticket_range[0] * days,
            accommodation[1] * days + food[1] * days + transport[1] * days + ticket_range[1] * days,
        ),
    }
